OUT_DIR.mkdir(parents=True, exist_ok=True)

print(f"-> Iniciando consolidación visual federal.")
print(f"-> Leyendo base maestra de 3.3M de registros en: {ARCHIVO_LIMPIO}")

def generar_consolidado():
    try:
        # CARGA Y PREPARACIÓN DE DATOS 
        df = pd.read_csv(ARCHIVO_LIMPIO)
        df['fecha'] = pd.to_datetime(df['fecha'])
        df['month'] = df['fecha'].dt.month

        #  BUCLE DE GENERACIÓN POR NODO 
        for ciudad in df['ciudad_id'].unique():
            print(f"   Procesando Nodo: {ciudad.upper()}...")
            
            # Filtrado de datos por ciudad
            datos = df[df['ciudad_id'] == ciudad].copy()
            
            # Obtención de la región operativa de CAMMESA para el nodo actual 
            reg_cammesa = mapeo_coordenadas.get(ciudad.lower(), 'CENTRO')
            huella = HUELLAS_CAMMESA.get(reg_cammesa)

           
            # Remuestreo mensual para suavizar la curva y capturar la estacionalidad real 
            oferta_mensual = datos.groupby('month')['radiacion_solar(ALLSKY)'].mean()
            
            # Normalización de la demanda según la huella de CAMMESA 2023 
            # (Se asume un consumo base normalizado para la visualización comparativa)
            meses_nombres = ['Ene', 'Feb', 'Mar', 'Abr', 'May', 'Jun', 'Jul', 'Ago', 'Sep', 'Oct', 'Nov', 'Dic']
            
            #  GENERACIÓN DEL GRÁFICO 
            plt.figure(figsize=(12, 6))
            
            # Graficar Oferta Solar Predicha/Histórica
            plt.plot(meses_nombres, oferta_mensual.values, marker='o', linewidth=2.5, 
                     color='#f39c12', label='Oferta Solar Promedio (Watts/m²)')
            
            # Graficar Huella de Demanda CAMMESA (Normalizada para ver el cruce)
            # Multiplicamos la huella por un factor de escala para que sea comparable visualmente
            factor_escala = oferta_mensual.mean() 
            demanda_visual = [h * factor_escala for h in huella]
            
            plt.plot(meses_nombres, demanda_visual, linestyle='--', color='#2c3e50', 
                     label=f'Perfil de Demanda Real (Región: {reg_cammesa})')

            # Estética y Formato Profesional
            plt.title(f"Balance Energético Consolidado: {ciudad.upper()}", fontsize=14, fontweight='bold')
            plt.ylabel("Intensidad Energética (Escala Normalizada)")
            plt.xlabel("Ciclo Estacional (Meses)")
            plt.grid(True, alpha=0.3, linestyle='--')
            plt.legend(loc='upper right')
            
            # Guardado automático en alta resolución
            nombre_archivo = OUT_DIR / f"consolidado_{ciudad}.png"
            plt.savefig(nombre_archivo, dpi=300, bbox_inches='tight')
            plt.close()

        print(f"\n PROCESO FINALIZADO. Los gráficos han sido guardados en: {OUT_DIR}")

    except FileNotFoundError:
        print(f" Error: El archivo {ARCHIVO_LIMPIO} no existe.")
    except Exception as e:
        print(f" Error inesperado: {e}")

if __name__ == "__main__":
    generar_consolidado()










































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































